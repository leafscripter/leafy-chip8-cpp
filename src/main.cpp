int main() {

	Emulator chip8;

	std::string fn;
	std::cout << "Enter valid name: ";
	std::cin >> fn;
	
	chip8.load_rom_into_memory(fn);

	sf::RenderWindow window(sf::VideoMode(64, 32), "Chip8");

	while (window.isOpen()) {
		sf::Event event;
		while (window.pollEvent(event)) {
			if (event.type == sf::Event::Closed)
				window.close();
		}

		chip8.PerformCpuCycle();
	}

	return 0;
}
